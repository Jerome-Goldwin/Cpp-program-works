#include<iostream>

using namespace std;

class Soldier{
	private:
		string name;
		int age;
		float stamina;

	public:
		Soldier(string n, int a, float s){
			name=n;
			age=a;
			stamina=s;
		}
		void displayInfo(){
			cout << "\nSoldier Report" << endl;
			cout << "---------------" << endl;
			cout << "Name: " << name << endl;
			cout << "Age: " << age << endl;
			cout << "Stamina: " << stamina << endl;
		}
};

int main(){
	Soldier s1("Ghost", 32, 87.5);
	Soldier s2("Soap", 29, 92.1);
	s1.displayInfo();
	s2.displayInfo();
	return 0;
}
